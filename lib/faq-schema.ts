// Server-safe FAQ schema generator
export interface FAQItem {
  question: string
  answer: string
}

// Generate FAQ Schema for SEO - this can be used in server components
export function generateFAQSchema(faqs: FAQItem[]) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: faqs.map((faq) => ({
      "@type": "Question",
      name: faq.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: faq.answer,
      },
    })),
  }
}
